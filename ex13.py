num=int(input('Insira um numero de 1 a 7: '))
if num==1:
    print('1-Domingo')
elif num==2:
    print('2-Segunda')
elif num==3:
    print('3-Terca')
elif num==4:
    print('4-Quarta')
elif num==5:
    print('5-Quinta')
elif num==6:
    print('6-Sexta')
elif num==7:
    print('7-Sabado')
else:
    print('Valor Invalido')

